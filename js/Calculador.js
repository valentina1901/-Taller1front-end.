

/* para dar las operaciones se crearan una clase con sus operaciones */
class Calculador{
    
    //metodos
    sumar(num1, num2){
        return num1 + num2;
    }

    restar(num1, num2){
        return num1 - num2;
    }

    multiplicar(num1, num2){
        return num1 * num2;
    }

    dividir(num1, num2){
        return num1 / num2;
    }
}